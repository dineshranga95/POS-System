     */
    public function store(Request $request)
    {
        $product = new Product();
        $product->name=$request->name;
        $product->barcode=$request->barcode;
        $product->price=$request->price;
        $product->save();
        $products=Product::latest()->paginate(5);
        return view ('products.index')->with('list',$products);
    }

